
public class forLoop1 {

	public static void main(String[] args) {
		int i;
		i = 1;
		System.out.println("Value of variable i is " + i);
		
		i = i+1;
		System.out.println("Value of variable i is " + i);
		
		i = i+1;
		System.out.println("Value of variable i is " + i);
		
		i = i+1;
		System.out.println("Value of variable i is " + i);
		
		i = i+1;
		System.out.println("Value of variable i is " + i);
		
		i = i+1;
		System.out.println("Value of variable i is " + i);
		
		
		for (int j = 1; j <501; j = j + 1) {
			System.out.println("!!!!!!!!!!!!");
			System.out.println("Value of j is " + j);
			System.out.println("!!!!!!!!!!!!");
		}
		

	}

}
